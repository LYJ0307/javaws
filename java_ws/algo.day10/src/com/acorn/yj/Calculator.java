package com.acorn.yj;

public class Calculator {

	public static int add(int su1, int su2) {
		
		return su1 + su2;
		
	}
	
	
	public static int sub(int su3, int su4) {
		
		return su3 - su4 ;
		
	}
	
	
	public static int mul(int su5, int su6) {
		
		return su5 * su6 ;
	}
	
	
	public static int div(int su7, int su8) {
		
		return su7 / su8;
		
	}

}
